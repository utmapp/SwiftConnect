	func testConnectSharedKey() async throws {
		let server = Task {
			let connection = try await Connection(connection: Connection.advertise(forServiceType: Self.serviceType, key: Self.key).first { _ in true }!)
			try await Self.verify(sending: Self.serverData, receiving: Self.clientData, on: connection)
			connection.close()
		}
		let client = Task {
			let connection = try await Connection(endpoint: Connection.endpoints(forServiceType: Self.serviceType).first { !$0.isEmpty }!.first!, key: Self.key)
			try await Self.verify(sending: Self.clientData, receiving: Self.serverData, on: connection)
			connection.close()
		}
		try await client.value
		try await server.value
	}

	func testConnectCertificate() async throws {
		let importOptions = [ kSecImportExportPassphrase as String: "password" ]
		var rawItems: CFArray?
		XCTAssert(SecPKCS12Import(clientP12 as CFData, importOptions as CFDictionary, &rawItems) == errSecSuccess)
		XCTAssertNotNil(rawItems)
		let clientItems = rawItems! as! [[String: Any]]
		let clientIdentity = clientItems[0][kSecImportItemIdentity as String] as! SecIdentity
		let clientCertChain = clientItems[0][kSecImportItemCertChain as String] as! [SecCertificate]
		let clientCertData = SecCertificateCopyData(clientCertChain[0])

		XCTAssert(SecPKCS12Import(serverP12 as CFData, importOptions as CFDictionary, &rawItems) == errSecSuccess)
		XCTAssertNotNil(rawItems)
		let serverItems = rawItems! as! [[String: Any]]
		let serverIdentity = serverItems[0][kSecImportItemIdentity as String] as! SecIdentity
		let serverCertChain = serverItems[0][kSecImportItemCertChain as String] as! [SecCertificate]
		let serverCertData = SecCertificateCopyData(serverCertChain[0])

		let server = Task {
			let connection = try await Connection(connection: Connection.advertise(forServiceType: Self.serviceType, identity: serverIdentity).first { _ in true }!)
			try await Self.verify(sending: Self.serverData, receiving: Self.clientData, on: connection)
			let chain = connection.peerCertificateChain
			XCTAssert(chain.count == 1)
			connection.close()
			return chain.first!
		}
		let client = Task {
			let connection = try await Connection(endpoint: Connection.endpoints(forServiceType: Self.serviceType).first { !$0.isEmpty }!.first!, identity: clientIdentity)
			try await Self.verify(sending: Self.clientData, receiving: Self.serverData, on: connection)
			let chain = connection.peerCertificateChain
			XCTAssert(chain.count == 1)
			connection.close()
			return chain.first!
		}
		let serverCert = try await client.value
		let clientCert = try await server.value
		let peerServerCertData = SecCertificateCopyData(serverCert)
		let peerClientCertData = SecCertificateCopyData(clientCert)
		XCTAssertEqual(clientCertData, peerClientCertData)
		XCTAssertEqual(serverCertData, peerServerCertData)
	}

	static func verify(sending data: [Data], receiving: [Data], on connection: Connection) async throws {
		let send = Task {
			for d in data {
				try await connection.send(data: d)
			}
		}
		let receive = Task {
			var results = [Data]()
			for try await data in connection.data {
				results.append(data)
				if results.count == receiving.count {
					break
				}
			}
			return results
		}
		let results = try await (send.value, receive.value).1
		XCTAssertEqual(results, receiving)
	}

